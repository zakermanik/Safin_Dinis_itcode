/*
* Написать функцию, которая принимает массив чисел, например [1,2,3,4,5] и функция возращает среднее арифметическое, (округлить результат до десятых)
* */

function abs(array) {
    let sum = 0;
    array.forEach(a => {
        sum += a;
    });
    return sum/2
}









































module.exports = abs